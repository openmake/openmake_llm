/**
 * 대화체 채팅 메시지 → 검색 키워드 쿼리 정제 (의존성 없는 순수 함수).
 *
 * 자동 웹검색은 사용자 메시지 전체를 그대로 검색 쿼리로 쓰는데, "지금 대한민국 대통령이
 * 누구인지 웹 검색 결과를 바탕으로 한 문장으로 알려줘." 같은 장황한 지시문이 쿼리에 섞이면
 * 뉴스 API(Naver/News)가 0건을 반환하고 일반 백과 페이지만 남아 시의성 있는 사실(현직 인물 등)을
 * 놓친다. 알려진 지시 문구만 보수적으로 제거해 핵심 키워드를 남긴다. (MCP 도구/fact-check 의
 * 명시적 쿼리는 정제하지 않음 — 이 정제는 chat 자동검색 경로에서만 적용한다.)
 *
 * @module mcp/web-search/query-cleaner
 */

/** 제거 대상 대화체 지시 문구 패턴 (한국어 중심). 보수적으로만 — 핵심 명사는 보존. */
const INSTRUCTION_NOISE: readonly RegExp[] = [
    /웹\s*검색(\s*결과)?(을|를|은|는)?\s*(바탕으로|참고(해서|하여)?|통해|이용(해서|하여)?)?/g,
    /검색(\s*결과)?(을|를)?\s*(바탕으로|참고(해서|하여)?)?/g,
    /(한|두|세|짧은)?\s*문장으로/g,
    /(간단히|자세히|상세히|짧게|요약해서|정리해서)/g,
    /(알려|말해|설명해|답해|정리해|가르쳐)\s*(줘|주세요|줄래|주실래요|봐)?/g,
    /(이|가|은|는)?\s*누구(야|인지|니|예요|입니까|인가요|신가요)?\s*\??/g,
    /(이|가|은|는)?\s*뭐(야|예요|니)?\s*\??/g,
    /무엇(인지|입니까|인가요)?\s*\??/g,
];

/**
 * 대화체 메시지에서 지시 문구를 제거해 검색 키워드만 남긴다.
 * 과잉 제거로 빈 문자열이 되면 원문을 그대로 반환(안전망).
 */
export function cleanSearchQuery(raw: string): string {
    const original = (raw || '').trim();
    let q = original;
    for (const re of INSTRUCTION_NOISE) q = q.replace(re, ' ');
    q = q
        .replace(/\s+/g, ' ')
        .replace(/^[\s.?!,]+/g, '')
        .replace(/[\s.?!,]+$/g, '')
        .trim();
    return q.length >= 2 ? q : original;
}
